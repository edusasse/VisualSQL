    private boolean verfCampos() {
        if (this.txtConnName.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Faltou preencher o nome da conexao!");
            return false;
        } else if (this.txtUrl.getText().trim().equals("") || this.jcmLibaries.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Faltou preencher a URL de conexao!");
            return false;
        }
        return true;
    }

