    private void setEditing() {
        this.editing = true;
    }

    private boolean isEditing() {
        return editing;
    }

    public void setEdit(ConnectionHandler ch) {
        this.setEditing();
        this.txtConnName.setText(ch.getKey());
        this.txtConnName.setEditable(false);

        for (int i = 0; i < this.jcmLibaries.getItemCount(); i++) {
            if (this.jcmLibaries.getItemAt(i).toString().equals(ch.getDriverClass().toString())) {
                this.jcmLibaries.setSelectedIndex(i);
            }
        }

        this.txtUrl.setText(ch.getUrl());
        this.ckSavePass.setSelected(ch.isSavePassword());
        this.ckAutoConn.setSelected(ch.isAutoConnectOnOpen());
        this.ckAutoSaveOnExit.setSelected(ch.isAutoSaveOnExit());
        this.ckLoadMetaDataOnConnect.setSelected(ch.isLoadMetaDataOnConnect());
        this.txtUser.setText(ch.getInfo().getProperty("user"));
        this.txtPass.setText(ch.getInfo().getProperty("password"));
    }
