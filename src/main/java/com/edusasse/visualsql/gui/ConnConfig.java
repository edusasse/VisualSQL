    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (this.verfCampos() == true) {
            Hashtable<String, Object> par = new Hashtable<String, Object>();
            par.put("AUTO_SAVE_ON_EXIT", new Boolean(this.ckAutoSaveOnExit.isSelected()));
            par.put("SAVE_PASSWORD", new Boolean(this.ckSavePass.isSelected()));
            par.put("AUTO_CONNECT_ON_OPEN", new Boolean(this.ckAutoConn.isSelected()));
            par.put("LOAD_META_DATA", new Boolean(this.ckLoadMetaDataOnConnect.isSelected()));
            try {
                if (isEditing()) {
                    ConnController.removeHandler(this.txtConnName.getText().trim());
                }
                ConnController.open(this.jcmLibaries.getSelectedItem().toString(), this.txtConnName.getText().trim(), this.txtUrl.getText(), this.txtUser.getText().trim(), String.valueOf(this.txtPass.getPassword()), par);
                GUIController.getInstance().getMain().removeConn(this);
                tree.newConnAdd();

            } catch (Exception ex) {
                Logger.getLogger(ConnConfig.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

        GUIController.getInstance().saveProfileConn();
    }//GEN-LAST:event_jButton1ActionPerformed

